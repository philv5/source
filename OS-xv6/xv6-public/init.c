// init: The initial user-level program

#include "types.h"
#include "stat.h"
#include "user.h"
#include "fcntl.h"

char *argv[] = { "sh", 0 };

int
main(void)
{
  int pid, wpid;

  if(open("console", O_RDWR) < 0){
    mknod("console", 1, 1);
    open("console", O_RDWR);
  }
  dup(0);  // stdout
  dup(0);  // stderr

  #ifdef DEFAULT
    printf(1,"schedular policy: Round-Robin\n");
  #elif FCFS_SCHED
    printf(1,"schedular policy: FCFS\n");
  #elif PRIORITY_SCHED
    printf(1,"schedular policy: Priority\n");
  #elif MLFQ_SCHED
    printf(1,"schedular policy: MLFQ\n");
  #endif

  for(;;){
    printf(1, "init: starting sh\n");
    pid = fork();
    if(pid < 0){
      printf(1, "init: fork failed\n");
      exit();
    }
    if(pid == 0){
      exec("sh", argv);
      printf(1, "init: exec sh failed\n");
      exit();
    }
    while((wpid=wait()) >= 0 && wpid != pid)
      printf(1, "zombie!\n");
  }
}
